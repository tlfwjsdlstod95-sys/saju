function sum(terms: Term[], tau: number): number {
  let s = 0;
  for (const [a, b, c] of terms) s += a * Math.cos(b + c * tau);
  return s;
}

const DEG = Math.PI / 180;

/** 태양의 겉보기 황경(도). jde는 TT 기준 JD. VSOP87 정밀. */
export function sunApparentLongitudeVSOP(jde: number): number {
  const tau = (jde - 2451545.0) / 365250.0;
  const T = tau * 10; // 율리우스 세기

  // 지구 일심 황경 L (radian)
  const L = (sum(L0, tau) + sum(L1, tau) * tau + sum(L2, tau) * tau ** 2
    + sum(L3, tau) * tau ** 3 + sum(L4, tau) * tau ** 4 + sum(L5, tau) * tau ** 5) / 1e8;
  // 지구-태양 거리 R (AU)
  const R = (sum(R0, tau) + sum(R1, tau) * tau + sum(R2, tau) * tau ** 2) / 1e8;

  // 지심 태양 황경 Θ = L + 180°
  let theta = (L / DEG) + 180;
  // FK5 보정 (Meeus 25.9)
  const Lp = theta - 1.397 * T - 0.00031 * T * T;
  const dLfk5 = -0.09033 / 3600 + (0.03916 / 3600) * (Math.cos(Lp * DEG) - Math.sin(Lp * DEG)) * Math.tan(0); // 위도 0 가정 → 0
  theta = theta + dLfk5;

  // 章動(nutation in longitude) 주요 2항
  const omega = 125.04452 - 1934.136261 * T;
  const Lsun = 280.4665 + 36000.7698 * T;
  const dpsi = (-17.20 * Math.sin(omega * DEG) - 1.32 * Math.sin(2 * Lsun * DEG)) / 3600; // 도

  // 광행차 (aberration): -20.4898" / R
  const aberr = -(20.4898 / 3600) / R;

  const lambda = theta + dpsi + aberr;
  return ((lambda % 360) + 360) % 360;
}
